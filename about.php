<h3 style="display: table;margin: 0 auto;font-size: 25px;">ABOUT CENTRAL GROUND WATER AUTHORITY</h3><br><br><hr>
<br>

<p style="margin-left: 5%;font-size: 20px;">Central Ground Water Authority has been constituted under Section 3 (3) of the Environment (Protection) Act, 1986 to
regulate and control development and management of ground water resources in the country.</p>
<br><hr><br>
<span style="margin-left: 3%;font-weight: bold; font-size: 20px;">Powers & Functions:</span> 
<br>
<br>
<p style="margin-left: 5%;font-size: 20px;">The Authority has been conferred with the following powers: 
</p> 
<br>
<p style="margin-left: 5%;font-size: 20px;">
(i)   Exercise of powers under section 5 of the Environment (Protection) Act, 1986 for issuing directions and taking such measures in respect of all the matters referred to in sub-section(2) of section 3 of the said Act.<br>
(ii)   To resort to penal provisions contained in sections 15 to 21 of the said Act.<br>
(iii)  To regulate and control, management and development of ground water in the country and to issue necessary regulatory directions for the purpose.<br>
(iv)  Exercise of powers under section 4 of the Environment (Protection) Act, 1986 for the appointment of officers.<br></p>
<br>
<br>
<span style="margin-left: 3%;font-weight: bold; font-size: 20px;" >Regulatory measures :</span>
<br>
<br>
<ul style="margin-left: 4%;font-size: 20px;">
<li>The Central Ground Water Authority is regulating withdrawal of ground water by industries/ projects in 802 Over-exploited and 169 Critical Assessment Units. List of these critical areas has been circulated to the State Pollution Control Boards and Ministry of Environment & Forests which refer the new industries/ projects to CGWA for obtaining permission.</li>
<li>CGWA has notified 162 critical/ overexploited areas in parts of NCT Delhi, Haryana, Punjab, Andhra Pradesh, Rajasthan, MP, Gujarat, West Bengal, Uttar Pradesh, Karnataka, Tamil Nadu, UT of Puducherry and UT of Diu for control and regulation of development of ground water resources. For enforcement of the regulatory measures in these areas, concerned Deputy Commissioners/ District Magistrates have been directed under Section 5 of Environment (Protection) Act, 1986 to regulate ground water development in these notified areas.</li>
<li>Construction of new ground water structures is prohibited in the notified areas. Permission of drilling tubewells is being granted only to the Govt. agencies responsible for drinking water supply.</li>
</ul>
<br>
<br>
<span style="margin-left: 3%;font-weight: bold; font-size: 20px;"> Ground Water Legislation :</span>
<br>
<br>
<p style="margin-left: 5%;font-size: 20px;">
To enable the States to enact Ground Water Legislation, a Model Bill to Regulate and Control Development of Ground Water has been circulated by the Ministry OF Water Resources to all the States/UTs. So far the states of Andhra Pradesh, Goa, Tamil Nadu, Kerala, West Bengal, Himachal Pradesh and Union Territories of Lakshadweep and Pondicherry have enacted and implemented ground water legislation.</p>
